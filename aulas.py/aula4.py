# Tipos int e float
# int -> Numero inteiro
# O tipo int representa qualquer numero
# positivo ou negativo. int sem sinal é considerado positivo.
print(11) # int
print(-11) # int
print(0) #int

# float -> Numero com ponto flutuante
# O tipo float representa qualquer numero
# positivo ou negativo com pontos flutuante.
# float sem sinao é considerado positivo.
print(1.1, 10.11) # float (sempre separa os numeros com ".", isso é para qualquer linguagem)
print(0.0, -1.5)



# A função tupe mostra o tipo que o Python
# inferiu ao valor.
print(type('Diego')) 
print(type(1))
print(type(1.1), type(-1.1), type(0.0)) 

