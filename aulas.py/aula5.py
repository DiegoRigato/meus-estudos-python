# Tipo de dadp bool (boolean)
# Ao questionar algo em um programa,
# só existem duas respostas possiveis:
# sim (True) ou não (False).
# Existe varios operadores para "questionar".
# Dentre eles o ==, que é um operador logico que
# questina se um valor é igual o outro.
print(10 == 10) # Sim => True (Verdadeiro)
print(10 == 11) # Não => False (Falso)
print(type(True))
print(type(False))
print(type(10 == 10))
print(type(10 == 11))

